				<p><strong>DIA NACIONAL DO IDOSO: A REFLEXÃO QUE SE IMPÕE</strong></p>
				
				<p>O dia nacional do idoso, que se comemora a 30 de Novembro, foi instituído em sinal de respeito e reconhecimento dos feitos de angolanos que se encontram na terceira idade e que muito contribuíram para o engrandecimento do país.</p>
				
				<p>Aproveitamos esta data para mais uma vez trazer para o debate algumas questões relacionadas a esta franja da população, já abordadas no nosso livro "Qualidade de vida e bem-estar no pós-carreira: contribuições dos programas de preparação para a reforma".</p>
				
				<p>Começamos por considerar que o aumento da esperança de vida é um dos maiores triunfos da humanidade, mas também um dos grandes desafios, na medida em que vai causando um aumento das demandas sociais e económicas. Logo, levantam-se algumas questões fundamentais para os formuladores de políticas e para os profissionais que se dedicam a estudar a população idosa: como podemos ajudar as pessoas a permanecerem independentes e autónomas à medida que envelhecem? Como a qualidade de vida e o bem-estar na velhice podem ser melhorados?</p>
				
				<p>Deve ser preocupação de todos nós promover o envelhecimento saudável, para que as pessoas idosas sejam um recurso cada vez mais valioso. É nesta perspectiva que a seguir apresentamos algumas reflexões, com vista a aclarar a temática e minimizar os constrangimentos para uma vida idosa mais saudável e activa.</p>
				
				<p>Em primeiro lugar advogamos a necessidade do Executivo, em parceria com os órgãos de comunicação social, mobilizar cada vez mais as organizações e os trabalhadores para a adesão ao sistema de Segurança Social e ao desenvolvimento de hábitos de poupança para o futuro.</p>
				
				<p>Importa referir que o idoso de hoje é o reflexo do jovem e do adulto que foi, pois é na idade mais avançada que o ser humano se depara com as consequências do que foi construindo ao longo da sua história social, profissional, física e afectiva. Assim, é necessário incentivar o desenvolvimento de atitudes de autocuidado, para que se tenha uma velhice saudável, por meio de hábitos saudáveis. Os adultos maduros que se preparam para o envelhecimento devem dar especial atenção ao autocuidado, à prevenção e ao controlo de doenças crónicas. Entre as atitudes de autocuidado destacam-se o controlo do peso, actividade física, alimentação saudável, saúde bucal (saúde oral), uso adequado de medicamentos e outras substâncias químicas, prevenção de quedas, optimização da capacidade mental, vacinação e cuidados com a pele.</p>
				
				<p>Ainda em relação à saúde, ressalta-se a necessidade de o Executivo priorizar uma rede de serviços básicos de saúde para a população, de modo geral, com serviços específicos para os idosos. A incidência de doenças crónicas em Angola tende a aumentar, exigindo cuidados especiais e um novo modelo de atenção à saúde para a população que está a envelhecer.</p>
				
				<p>Outro tópico importante a considerar é a educação ao longo da vida, especialmente sob o ponto de vista do estímulo cognitivo que oferece, como coadjuvante no bem-estar na terceira idade. Entretanto, a educação ao longo da vida também pode representar um novo começo, na medida em que as pessoas necessitam de se actualizar para continuarem no mercado de trabalho ou retornar a ele. Sem dúvidas, educação é um dos aspectos responsáveis pelo bem-estar subjectivo e está correlacionado com uma série de outros índices de qualidade de vida, como a manutenção da saúde (França, 2012).</p>
				
				<p>Ressalta-se também o papel relevante que pode ser desempenhado pelos órgãos de comunicação social, pelas agências de turismo e pelos promotores culturais. Ao incluir, nas suas agendas, conteúdos, informações ou até mesmo programas voltados para pessoas idosas, os órgãos de comunicação estarão a contribuir para uma melhor qualidade de vida e bem-estar dessa população. Por outro lado, a mídia pode desempenhar o papel de quebrar preconceitos sobre a velhice e o reformado, muito presentes na sociedade, de modo geral, e nas organizações de forma específica.</p>
				
				<p>As agências de viagem, os promotores culturais e ginásios, também têm um papel crucial, principalmente na ocupação dos tempos livres dos idosos. Assim como os aspectos financeiros, saúde e relacionamento familiar, a distribuição do tempo entre actividades diversificadas, é um dos preditores mais importantes para atitudes positivas face ao envelhecimento. É necessário que sejam criados pacotes turísticos, com bonificações, assim como eventos culturais que promovam o lazer, e programas de educação física que promovam a saúde.</p>
				
				<p>Em relação à família, torna-se necessário reflectirmos sobre a inserção do idoso na esfera familiar e as relações de suporte e fluxo de recursos ali existentes, pois ela tem um papel determinante enquanto esfera para a maximização ou para a limitação de um envelhecimento saudável e com qualidade de vida. Interdependência e solidariedade entre gerações são princípios relevantes para o envelhecimento activo. A criança de ontem é o adulto de hoje e o idoso de amanhã. Desta forma, a qualidade de vida e o bem-estar que as pessoas terão enquanto idosas depende não só dos riscos e oportunidades que experimentarem durante a vida, mas também da maneira como as gerações posteriores irão oferecer ajuda e apoio mútuos, quando necessário.</p>
				
				<p>Em relação às políticas para o idoso, destaca-se a necessidade de programas e serviços que capacitem as pessoas a permanecerem em casa durante a velhice, com ou sem outros familiares, de acordo com as circunstâncias e preferências. Para o efeito, devem ser garantidos apoios às famílias que incluem idosos e que precisam de cuidados em seu lar.</p>
				
				<p>Finalmente, chama-se atenção para a recomendação da OMS, segundo a qual os países podem custear o envelhecimento se os governos, as organizações internacionais e a sociedade civil implementarem políticas e programas de "envelhecimento activo" que melhorem a saúde, a participação e a segurança dos cidadãos mais velhos. As políticas e programas acima referidos devem ser baseados nos direitos, necessidades, preferências e habilidades das pessoas mais velhas. Devem incluir, também, uma perspectiva de curso de vida que reconheça a importante influência das experiências de vida para a maneira como os indivíduos envelhecem.</p>
				
				<p>Ao trazemos estas reflexões, consideramos, acima de tudo, a necessidade de olharmos para os nossos idosos como recursos importantes para a estrutura e desenvolvimento da nossa sociedade, uma vez que podem continuar a contribuir activamente para o país, para as suas comunidades e para as suas famílias.</p>
				
